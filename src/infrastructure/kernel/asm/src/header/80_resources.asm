;---------------------------------------------------------------------------------------------------------
; RESOURCES CODE
; Resource map structure (starting at 0010h on rom memory):
;   word resource_count
;   array resources[]
;      word resource_pointer
;      word resource_size
;---------------------------------------------------------------------------------------------------------

; out: de = resource count
;      hl = start of resource map pointers
resource.count:
  ld hl, (RSCMAPAD)
  ld e, (hl)
  inc hl
  ld d, (hl)
  inc hl
  ret

; in:  bc = resource number
; out: hl = resource address
;      bc = resource size
;      a  = segment
resource.address:
  call resource.count

  dec de

  ld a, d
  cp b
  ret c       ; return if resource number > resource count
  jr nz, resource.address.next

  ld a, e
  cp c
  ret c       ; return if resource number > resource count

resource.address.next:
  ex de, hl
    ld l, c
    ld h, b
    add hl, hl  ; x 2
    add hl, hl  ; x 4
    add hl, bc  ; x 5
  ex de, hl

  add hl, de

  xor a       ; clear carry
  ld e, (hl)  ; get resource address
  inc hl
  ld d, (hl)
  inc hl
  ld a, (hl)  ; get segment number
  inc hl
  ld c, (hl)  ; get resource size
  inc hl
  ld b, (hl)

  or a
  call nz, MR_CHANGE_SGM

  ex de, hl
  ret

; in:  bc = resource number
; out: hl = data pointer
resource.get_data:
  di
    push bc
      call resource.open
    pop bc

    call resource.address    ; hl = resource start address, a = segment, bc = size

    ld de, BUF
    ld bc, 255
    ldir

    call resource.close
  ei
  ld hl, BUF
  ret

; in:  hl = pletter data
;      de = ram address
; out: hl = ram address
;      bc = size
resource.ram.unpack:
  push de
    call pletter.unpack
    exx       ; calculate decoded length (de' point to end of decoded data)
    ex de, hl
  pop de
  push de
    xor a
    sbc hl, de
    ld b, h
    ld c, l                 ; bc = decoded length
  pop hl                    ; hl = decoded data
  ret

